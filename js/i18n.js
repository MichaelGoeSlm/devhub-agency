/**
 * Initialize i18n system
 */
function initI18n() {
    // Get saved language or detect from browser
    let lang = localStorage.getItem('devhub-lang');
    if (!lang) {
        const browserLang = navigator.language.slice(0, 2);
        lang = ['fr', 'en', 'es'].includes(browserLang) ? browserLang : 'fr';
    }
    
    // Apply translations
    setLanguage(lang);
    
    // Update language buttons
    updateLangButtons(lang);
}

/**
 * Set the current language
 */
function setLanguage(lang) {
    if (!translations[lang]) lang = 'fr';
    
    localStorage.setItem('devhub-lang', lang);
    document.documentElement.lang = lang;
    
    // Translate all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Handle different element types
            if (el.tagName === 'OPTION') {
                el.textContent = translations[lang][key];
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });
    
    // Translate placeholders with data-i18n-placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });
    
    // Update meta description if available
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && translations[lang].meta_description) {
        metaDesc.content = translations[lang].meta_description;
    }
    
    // Update page title if available
    const pageKey = document.body.getAttribute('data-page');
    if (pageKey && translations[lang][pageKey + '_title']) {
        document.title = translations[lang][pageKey + '_title'];
    }
    
    updateLangButtons(lang);
}

/**
 * Update language selector buttons and display
 */
function updateLangButtons(lang) {
    // Update button active states
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
    
    // Update current language display in navbar
    const langCurrent = document.querySelector('.lang-current');
    if (langCurrent) {
        langCurrent.textContent = lang.toUpperCase();
    }
}

// Initialize on DOM ready
document.addEventListener('DOMContentLoaded', initI18n);
